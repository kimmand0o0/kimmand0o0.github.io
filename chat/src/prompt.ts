export function buildUserPrompt(question: string, excerpts: Array<{ title: string; chunk: string }>): string {
  const context = excerpts
    .map((e, i) => `[발췌 ${i + 1} — ${e.title}]\n${e.chunk}`)
    .join('\n\n');

  return `참고 글 발췌:\n${context}\n\n질문: ${question}`;
}
